<?php

declare(strict_types=1);

namespace Kami\Cocktail\Http\Controllers;

use Kami\Cocktail\Models\Tag;
use OpenApi\Attributes as OAT;
use Prism\Prism\Facades\Prism;
use Kami\Cocktail\OpenAPI as BAO;
use Kami\Cocktail\Models\Cocktail;
use Illuminate\Support\Facades\Log;
use Kami\Cocktail\External\Model\Schema;
use Kami\Cocktail\Models\CocktailMethod;
use Kami\Cocktail\GenAI\CocktailTagsHandler;
use Kami\Cocktail\GenAI\GenAIProviderConfig;
use Kami\Cocktail\Http\Resources\ImageResource;
use Kami\Cocktail\GenAI\DTO\CocktailTagsRequest;
use Kami\Cocktail\GenAI\CompleteIngredientHandler;
use Kami\Cocktail\GenAI\CocktailRecipeTextImportHandler;
use Kami\Cocktail\Http\Requests\CompleteIngredientRequest;
use Kami\Cocktail\GenAI\DTO\CocktailRecipeTextImportRequest;
use Kami\Cocktail\Http\Requests\CompleteCocktailTagsRequest;
use Prism\Prism\Exceptions\PrismStructuredDecodingException;
use Kami\Cocktail\Http\Requests\GenerateCocktailImageRequest;
use Kami\Cocktail\Http\Requests\CocktailRecipeFromTextRequest;
use Kami\Cocktail\Services\Image\CocktailImageGenerationService;
use Kami\Cocktail\Http\Resources\Generated\GeneratedIngredientResource;
use Kami\Cocktail\Http\Resources\Generated\GeneratedCocktailTagsResource;
use Kami\Cocktail\Http\Resources\Generated\GeneratedCocktailFromTextResource;
use Kami\Cocktail\GenAI\DTO\CompleteIngredientRequest as CompleteIngredientRequestDTO;

class GenerateController extends Controller
{
    #[OAT\Post(path: '/generate/ingredient', tags: ['Generation'], operationId: 'completeIngredient', description: 'Suggested ingredient information based on it\'s name, generated by an LLM.', summary: 'Complete ingredient', parameters: [
        new BAO\Parameters\BarIdHeaderParameter(),
    ], requestBody: new OAT\RequestBody(
        required: true,
        content: [
            new OAT\JsonContent(
                type: 'object',
                properties: [
                    new OAT\Property(property: 'name', type: 'string', example: 'Vodka', description: 'Name of the ingredient to complete information for'),
                ],
                required: ['name'],
            ),
        ]
    ))]
    #[BAO\SuccessfulResponse(content: [
        new BAO\WrapObjectWithData(GeneratedIngredientResource::class),
    ])]
    public function completeIngredient(CompleteIngredientRequest $request): GeneratedIngredientResource
    {
        $ingredientName = $request->input('name');
        $providerConfiguration = GenAIProviderConfig::fromConfig();
        $promptConfiguration = new CompleteIngredientHandler(new CompleteIngredientRequestDTO(
            $ingredientName,
        ))();

        Log::info("[LLM] Generating ingredient information for: {$ingredientName}");

        $response = Prism::structured()
            ->using($providerConfiguration->provider, $providerConfiguration->model)
            ->withSchema($promptConfiguration->schema)
            ->withPrompt($promptConfiguration->prompt)
            ->withClientOptions($providerConfiguration->getClientOptions())
            ->asStructured();

        return new GeneratedIngredientResource($response->structured);
    }

    #[OAT\Post(path: '/generate/cocktail-tags', tags: ['Generation'], operationId: 'completeCocktailTags', description: 'Suggested cocktail tags, based on existing cocktail information, generated by an LLM.', summary: 'Cocktail tags', parameters: [
        new BAO\Parameters\BarIdHeaderParameter(),
    ], requestBody: new OAT\RequestBody(
        required: true,
        content: [
            new OAT\JsonContent(
                type: 'object',
                properties: [
                    new OAT\Property(property: 'cocktail_id', type: 'string', example: 'Vodka', description: 'Id or slug of the ingredient'),
                ],
                required: ['cocktail_id'],
            ),
        ]
    ))]
    #[BAO\SuccessfulResponse(content: [
        new BAO\WrapObjectWithData(GeneratedCocktailTagsResource::class),
    ])]
    public function completeCocktailTags(CompleteCocktailTagsRequest $request): GeneratedCocktailTagsResource
    {
        $cocktailId = $request->input('cocktail_id');
        $cocktail = Cocktail::where('slug', $cocktailId)
            ->orWhere('id', $cocktailId)
            ->firstOrFail();

        // Limit to top 50 most used tags to reduce token usage
        $existingTags = Tag::where('bar_id', $cocktail->bar_id)
            ->withCount('cocktails')
            ->orderByDesc('cocktails_count')
            ->limit(50)
            ->pluck('name')
            ->toArray();

        $cocktailMarkdown = Schema::fromCocktailModel($cocktail)->toMarkdown();

        $providerConfiguration = GenAIProviderConfig::fromConfig();
        $promptConfiguration = new CocktailTagsHandler(new CocktailTagsRequest(
            $cocktailMarkdown,
            $existingTags,
        ))();

        Log::info("[LLM] Generating cocktail tags for cocktail ID: {$cocktailId}");

        $response = Prism::structured()
            ->using($providerConfiguration->provider, $providerConfiguration->model)
            ->withSchema($promptConfiguration->schema)
            ->withPrompt($promptConfiguration->prompt)
            ->withClientOptions($providerConfiguration->getClientOptions())
            ->asStructured();

        return new GeneratedCocktailTagsResource($response->structured);
    }

    #[OAT\Post(path: '/generate/cocktail-recipe-from-text', tags: ['Generation'], operationId: 'cocktailRecipeFromText', description: 'Generate a parseable json recipe structure from raw text.', summary: 'Recipe from text', parameters: [
        new BAO\Parameters\BarIdHeaderParameter(),
    ], requestBody: new OAT\RequestBody(
        required: true,
        content: [
            new OAT\JsonContent(
                type: 'object',
                properties: [
                    new OAT\Property(property: 'recipe', type: 'string', example: 'My recipe details are here...', description: 'The raw text of the cocktail recipe to parse')
                ],
                required: ['recipe'],
            ),
        ]
    ))]
    #[BAO\SuccessfulResponse(content: [
        new BAO\WrapObjectWithData(GeneratedCocktailFromTextResource::class),
    ])]
    public function cocktailRecipeFromText(CocktailRecipeFromTextRequest $request): GeneratedCocktailFromTextResource
    {
        $textRecipe = trim((string) $request->input('recipe'));
        $allowedMethods = CocktailMethod::where('bar_id', bar()->id)->pluck('name')->toArray();

        $providerConfiguration = GenAIProviderConfig::fromConfig();
        $promptConfiguration = new CocktailRecipeTextImportHandler(new CocktailRecipeTextImportRequest(
            $textRecipe,
            $allowedMethods,
        ))();

        Log::info("[LLM] cocktailRecipeFromText: Generating cocktail recipe from text.");

        try {
            $response = Prism::structured()
                ->using($providerConfiguration->provider, $providerConfiguration->model)
                ->withSchema($promptConfiguration->schema)
                ->withPrompt($promptConfiguration->prompt)
                ->withClientOptions($providerConfiguration->getClientOptions())
                ->asStructured();
        } catch (PrismStructuredDecodingException $e) {
            Log::error("[LLM] cocktailRecipeFromText: Structured response error", [$e->getMessage()]);

            abort(400, 'Failed to parse the cocktail recipe. Please ensure the input is clear and try again.');
        }

        return new GeneratedCocktailFromTextResource($response->structured);
    }

    #[OAT\Post(path: '/generate/cocktail-image', tags: ['Generation'], operationId: 'generateCocktailImage', description: 'Generate a cocktail image using an image model and return it as an unassigned image.', summary: 'Cocktail image', parameters: [
        new BAO\Parameters\BarIdHeaderParameter(),
    ], requestBody: new OAT\RequestBody(
        required: true,
        content: [
            new OAT\JsonContent(
                type: 'object',
                properties: [
                    new OAT\Property(property: 'cocktail_id', type: 'string', example: 'negroni', description: 'Id or slug of the cocktail to generate an image for'),
                    new OAT\Property(property: 'style', type: 'string', nullable: true, example: 'editorial studio photo', description: 'Optional visual style hint for the generated image'),
                ],
                required: ['cocktail_id'],
            ),
        ]
    ))]
    #[BAO\SuccessfulResponse(content: [
        new BAO\WrapObjectWithData(ImageResource::class),
    ])]
    public function generateCocktailImage(GenerateCocktailImageRequest $request, CocktailImageGenerationService $cocktailImageGenerationService): ImageResource
    {
        $cocktailId = $request->string('cocktail_id')->toString();

        $cocktail = Cocktail::query()
            ->where('bar_id', bar()->id)
            ->where(function ($query) use ($cocktailId) {
                $query->where('slug', $cocktailId)
                    ->orWhere('id', $cocktailId);
            })
            ->with('glass')
            ->firstOrFail();

        if ($request->user()->cannot('show', $cocktail)) {
            abort(403);
        }

        $image = $cocktailImageGenerationService->generateImage(
            cocktail: $cocktail,
            userId: $request->user()->id,
            style: $request->input('style'),
        );

        $cocktail->attachImages(collect([$image]));

        return new ImageResource($image);
    }
}
